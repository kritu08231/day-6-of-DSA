#Write a program to create a list of 6 fruit names and perform add, remove, sort, and reverse
#operations on the list.

basket=['Mango','Apple','Orange','Chiku','Banana','Watermelon']
print("List",basket)
basket.append('Grapes')
print("Append",basket)
basket.remove('Mango')
print("Removed",basket)
basket.sort()
print("sorted",basket)
basket.reverse()
print("reverse",basket)